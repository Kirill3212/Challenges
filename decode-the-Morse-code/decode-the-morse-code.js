decodeMorse = function (morseCode) {
  var ref = {
    ".-": "a",
    "-...": "b",
    "-.-.": "c",
    "-..": "d",
    ".": "e",
    "..-.": "f",
    "--.": "g",
    "....": "h",
    "..": "i",
    ".---": "j",
    "-.-": "k",
    ".-..": "l",
    "--": "m",
    "-.": "n",
    "---": "o",
    ".--.": "p",
    "--.-": "q",
    ".-.": "r",
    "...": "s",
    "-": "t",
    "..-": "u",
    "...-": "v",
    ".--": "w",
    "-..-": "x",
    "-.--": "y",
    "--..": "z",
    ".----": "1",
    "..---": "2",
    "...--": "3",
    "....-": "4",
    ".....": "5",
    "-....": "6",
    "--...": "7",
    "---..": "8",
    "----.": "9",
    "-----": "0",
    "...---...": "SOS",
    "/": " ",
    "-.-.--": "!",
    ".-.-.-": ".",
    "--..--": ",",
  };

  const arr = morseCode.split(" ");
  let result = [];

  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === "" && arr[i + 1] === "") {
      result.push(" ");
    }
    for (let code in ref) {
      if (code === arr[i]) {
        result.push(ref[code]);
      }
    }
  }

  return result.join("").toUpperCase().trim();
};

